package twoTreeFourTrees;

public class NodeshearchResult {
	private Node node;
	private int position;
	public NodeshearchResult(Node node, int position) {
		super();
		this.node = node;
		this.position = position;
	}
	public Node getNode() {
		return node;
	}
	public int getPosition() {
		return position;
	}
}
